package sg.edu.np.mad.p04_team4.AddFriend;
//class
public class Friend {
    private String name;
    private String birthday;

    public Friend(String name, String birthday) {
        this.name = name;
        this.birthday = birthday;
    }

    public String getName() {
        return name;
    }

    public String getBirthday() {
        return birthday;
    }
}
